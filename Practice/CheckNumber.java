//  2. Write a program to check if a number is even or odd using if-else.
package Practice;

public class CheckNumber {
    public static void main(String[] args) {
        int n =10;
        if (n%2==0) {
            System.out.println("Number is Even");
        }else{
            System.out.println("Number is odd");
        }
    }
    
}
